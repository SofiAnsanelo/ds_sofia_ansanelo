package Aula01;

class If {
    public static void main(String[] args){
        int x = 20;
        int y = 18;

        if (x > y){
            System.out.println("x e maior que y");
        } else if (x == y) {
            System.out.println("x e y sao iguais");
        } else {
            System.out.println("y e maior que x");
        }

    }
    
}
